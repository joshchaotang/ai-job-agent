"""
API: Slack Webhook Êé®ÈÄÅ
Êé®ÈÄÅËÅ∑‰ΩçÈÄöÁü•Âà∞ Slack È†ªÈÅì
"""
import json
import os
import requests
from http.server import BaseHTTPRequestHandler


class handler(BaseHTTPRequestHandler):
    def do_POST(self):
        try:
            content_length = int(self.headers.get('Content-Length', 0))
            post_data = self.rfile.read(content_length).decode('utf-8')
            data = json.loads(post_data)

            # ÂèñÂæó Slack Webhook URL
            webhook_url = os.environ.get('SLACK_WEBHOOK_URL', '')

            if not webhook_url:
                self.send_response(400)
                self.send_header('Content-Type', 'application/json')
                self.end_headers()
                self.wfile.write(json.dumps({
                    'success': False,
                    'error': 'Slack Webhook URL not configured'
                }).encode())
                return

            # ÂèñÂæóËÅ∑‰ΩçË≥áÊñô
            jobs = data.get('jobs', [])
            title = data.get('title', 'üéØ AI Job Agent Êé®Ëñ¶ËÅ∑‰Ωç')

            if not jobs:
                self.send_response(400)
                self.send_header('Content-Type', 'application/json')
                self.end_headers()
                self.wfile.write(json.dumps({
                    'success': False,
                    'error': 'No jobs provided'
                }).encode())
                return

            # Âª∫Á´ã Slack Ë®äÊÅØÊ†ºÂºèÔºàBlock KitÔºâ
            blocks = [
                {
                    "type": "header",
                    "text": {
                        "type": "plain_text",
                        "text": title,
                        "emoji": True
                    }
                },
                {
                    "type": "section",
                    "text": {
                        "type": "mrkdwn",
                        "text": f"ÊâæÂà∞ *{len(jobs)}* ÂÄãÊé®Ëñ¶ËÅ∑‰ΩçÔºà‰æùÂåπÈÖçÂ∫¶ÊéíÂ∫èÔºâ"
                    }
                },
                {"type": "divider"}
            ]

            # Âä†ÂÖ•ËÅ∑‰ΩçÂç°ÁâáÔºàTop 10Ôºâ
            for i, job in enumerate(jobs[:10], 1):
                match_score = job.get('match_score', 0)
                job_title = job.get('title', 'Untitled')
                company = job.get('company', 'Unknown')
                location = job.get('location', 'Remote')
                url = job.get('url', '')

                # Emoji Ê†πÊìöÂåπÈÖçÂ∫¶
                emoji = 'üî•' if match_score >= 85 else '‚≠ê' if match_score >= 70 else 'üìå'

                # ÂåπÈÖçÊäÄËÉΩ
                matched_skills = job.get('matched_skills', [])
                skills_text = f"\n‚ú® {', '.join(matched_skills[:3])}" if matched_skills else ""

                # ËÅ∑‰ΩçÂçÄÂ°ä
                job_block = {
                    "type": "section",
                    "text": {
                        "type": "mrkdwn",
                        "text": f"{emoji} *{i}. {job_title}*\n"
                                f"üè¢ {company} ¬∑ üìç {location}\n"
                                f"üéØ ÂåπÈÖçÂ∫¶Ôºö*{match_score}%*{skills_text}"
                    }
                }

                if url:
                    job_block["accessory"] = {
                        "type": "button",
                        "text": {
                            "type": "plain_text",
                            "text": "Êü•ÁúãËÅ∑‰Ωç",
                            "emoji": True
                        },
                        "url": url,
                        "style": "primary" if match_score >= 85 else "default"
                    }

                blocks.append(job_block)

            # ÂàÜÈöîÁ∑ö
            blocks.append({"type": "divider"})

            # È†ÅÂ∞æ
            blocks.append({
                "type": "context",
                "elements": [
                    {
                        "type": "mrkdwn",
                        "text": "_Generated by AI Job Agent v3.4.0_"
                    }
                ]
            })

            # ÁôºÈÄÅÂà∞ Slack
            payload = {
                "blocks": blocks,
                "text": title  # fallback text
            }

            response = requests.post(webhook_url, json=payload, timeout=10)
            response.raise_for_status()

            self.send_response(200)
            self.send_header('Content-Type', 'application/json')
            self.send_header('Access-Control-Allow-Origin', '*')
            self.end_headers()

            self.wfile.write(json.dumps({
                'success': True,
                'message': 'Successfully sent to Slack',
                'jobs_count': len(jobs)
            }).encode())

        except requests.exceptions.RequestException as e:
            self.send_response(500)
            self.send_header('Content-Type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({
                'success': False,
                'error': f'Slack API error: {str(e)}'
            }).encode())

        except Exception as e:
            self.send_response(500)
            self.send_header('Content-Type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({
                'success': False,
                'error': str(e)
            }).encode())

    def do_OPTIONS(self):
        self.send_response(200)
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        self.end_headers()
