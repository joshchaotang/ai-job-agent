import json
import os
import requests
from http.server import BaseHTTPRequestHandler


class handler(BaseHTTPRequestHandler):
    def do_POST(self):
        try:
            content_length = int(self.headers.get('Content-Length', 0))
            post_data = self.rfile.read(content_length).decode('utf-8')
            data = json.loads(post_data)

            # å–å¾—ç’°å¢ƒè®Šæ•¸
            bot_token = os.environ.get('TELEGRAM_BOT_TOKEN', '')
            chat_id = os.environ.get('TELEGRAM_CHAT_ID', '')

            if not bot_token or not chat_id:
                self.send_response(400)
                self.send_header('Content-Type', 'application/json')
                self.end_headers()
                self.wfile.write(json.dumps({
                    'success': False,
                    'error': 'Telegram credentials not configured'
                }).encode())
                return

            # å–å¾—è·ä½è³‡æ–™
            jobs = data.get('jobs', [])
            title = data.get('title', 'ğŸ¯ AI Job Agent æ¨è–¦è·ä½')

            if not jobs:
                self.send_response(400)
                self.send_header('Content-Type', 'application/json')
                self.end_headers()
                self.wfile.write(json.dumps({
                    'success': False,
                    'error': 'No jobs provided'
                }).encode())
                return

            # æ ¼å¼åŒ–è¨Šæ¯ï¼ˆTelegram Markdownï¼‰
            message = f"*{title}*\n\n"
            message += f"æ‰¾åˆ° *{len(jobs)}* å€‹æ¨è–¦è·ä½\n"
            message += "â”€" * 30 + "\n\n"

            for i, job in enumerate(jobs[:10], 1):  # é™åˆ¶ 10 ç­†ï¼ˆé¿å…è¨Šæ¯éé•·ï¼‰
                match_score = job.get('match_score', 0)
                job_title = job.get('title', 'Untitled')
                company = job.get('company', 'Unknown')
                location = job.get('location', 'Remote')
                url = job.get('url', '')

                # Emoji æ ¹æ“šåŒ¹é…åº¦
                emoji = 'ğŸ”¥' if match_score >= 85 else 'â­' if match_score >= 70 else 'ğŸ“Œ'

                message += f"{emoji} *{i}. {job_title}*\n"
                message += f"ğŸ¢ {company} Â· ğŸ“ {location}\n"
                message += f"ğŸ¯ åŒ¹é…åº¦ï¼š*{match_score}%*\n"

                # åŒ¹é…æŠ€èƒ½
                matched_skills = job.get('matched_skills', [])
                if matched_skills:
                    message += f"âœ¨ {', '.join(matched_skills[:3])}\n"

                if url:
                    message += f"[â†’ æŸ¥çœ‹è·ä½]({url})\n"

                message += "\n"

            message += "â”€" * 30 + "\n"
            message += "_Generated by AI Job Agent_"

            # ç™¼é€åˆ° Telegram
            telegram_url = f"https://api.telegram.org/bot{bot_token}/sendMessage"
            payload = {
                'chat_id': chat_id,
                'text': message,
                'parse_mode': 'Markdown',
                'disable_web_page_preview': True
            }

            response = requests.post(telegram_url, json=payload, timeout=10)
            response.raise_for_status()

            self.send_response(200)
            self.send_header('Content-Type', 'application/json')
            self.send_header('Access-Control-Allow-Origin', '*')
            self.end_headers()

            self.wfile.write(json.dumps({
                'success': True,
                'message': f'Successfully sent to Telegram (chat_id: {chat_id})',
                'jobs_count': len(jobs)
            }).encode())

        except requests.exceptions.RequestException as e:
            self.send_response(500)
            self.send_header('Content-Type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({
                'success': False,
                'error': f'Telegram API error: {str(e)}'
            }).encode())

        except Exception as e:
            self.send_response(500)
            self.send_header('Content-Type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps({
                'success': False,
                'error': str(e)
            }).encode())

    def do_OPTIONS(self):
        self.send_response(200)
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        self.end_headers()
